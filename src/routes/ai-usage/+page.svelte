    <h2 class="!mt-12 font-serif text-2xl font-semibold text-foreground">
      What we believe
    </h2>

    <ul class="list-none space-y-5 pl-0">
      <li>
        <strong>AI is a tool, not a replacement for human judgment.</strong> We use
        AI to research, process ideas, and execute tasks we could do ourselves, but
        appreciate that with the tool we can do it faster and with more readily available
        information. We take a "trust but verify" approach in our interactions with
        AI and are constantly evaluating how to improve our workflows.
      </li>
      <li>
        <strong>Every line of code is owned by us.</strong> We review everything we
        ship the way a tech lead reviews a developer's work: reading it, questioning
        it, often rewriting it. If it goes out with our name on it, we understand
        what it does and why.
      </li>
      <li>
        <strong>AI should raise the bar, not lower it.</strong> Faster
        implementation means more time for considering edge cases, honing
        documentation, and perfecting details. We hold AI-assisted work to a
        <em>higher</em> standard, and build in appropriate testing to back that up.
      </li>
      <li>
        <strong>"AI slop" is a process failure, not a tool failure.</strong> Poor
        output happens in the absence of skilled human attention. Taste, judgment,
        and experience are still what separate great work from garbage, and that will
        never change. We stay relentlessly in the loop.
      </li>
      <li>
        <strong>Craft still matters.</strong> Pixel-level decisions, typography, color,
        API design, the DX we'd want for ourselves...those responsibilities are still
        on us.
      </li>
      <li>
        <strong>We're still figuring it out.</strong> Things are moving fast. We've
        made mistakes. But these tenets are our foundation as we keep refining our
        process.
      </li>
    </ul>

